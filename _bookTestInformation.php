            <p class="bookTest-text text" style="color: purple; font-size:20px;">
              COVID-19 rapid lateral flow tests are no longer free for most people.</p>
            <p class="bookTest-text text">
              You may still be able to get free COVID-19 rapid lateral flow tests from the NHS if you have a health condition which means you're eligible for <a href="https://www.nhs.uk/conditions/covid-19/treatments-for-covid-19/">COVID-19 treatment</a></p>
            <p><strong>Information:</strong>
              If you want to get tested but are not eligible for free rapid lateral flow tests, you need to pay for the test yourself.</p>

            <p>You can buy rapid lateral flow tests from some pharmacies and retailers, in person or online.</p>

            <p><strong>How to get free COVID-19 rapid lateral flow tests if you're eligible</strong></p>
            <p>If you are eligible for COVID-19 treatments, you may be able to pick up free COVID-19 rapid lateral flow tests from your local pharmacy.</p>

            <p>You can find out if a pharmacy near you is offering free tests using the <a href="https://www.nhs.uk/service-search/pharmacy/find-a-pharmacy">Find a pharmacy service</a></p>

            <p><strong>How to check if a pharmacy offers free tests in the find a pharmacy service</strong></p>
              <ol>
                <li>Step 1 : Type your postcode, which will bring up a list of local pharmacies.</li>
                <li>Step 2 : Select a pharmacy.</li>
                <li>Step 3 : Click ‘Treatments and services’ at the top of the page.</li>
                <li>Step 4 :Go to the ‘Screening and test services’ section – if the pharmacy is offering free tests, it will say ‘COVID-19 lateral flows tests (eligible NHS patients)’.</li>
              </ol>

            <p>Your local integrated care board (ICB) may also be able to give you more information on where you can collect free tests.</p>

            <p><a href="https://www.nhs.uk/nhs-services/find-your-local-integrated-care-board/">Find your local integrated care board (ICB)</a></p>
